package models;

/**
 *
 * @author tuan anh le
 */
public class Item {

    protected int x;
    protected int y;
    protected int type;

    public Item(int x, int y, int type) {
        this.x = x;
        this.y = y;
        this.type = type;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public void setXY(int x, int y) {
        this.x = x;
        this.y = y;
    }

}
